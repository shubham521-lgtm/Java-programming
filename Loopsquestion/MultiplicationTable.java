public class MultiplicationTable {
    public static void main(String[] args) {
// Q3. Print the multiplication table of a number.
System.out.println("multiplication table of a number");
        int n = 7;
        for(int i = 1; i<=10; i++){
            System.out.println(7 + " X " + i + " = "+ n * i);
        }
    }
}
