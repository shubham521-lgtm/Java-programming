public class Long_to_float {
    public static void main(String[] args) {
// Q5. Convert float to long.
        float f = 6.7f;
         long l = (long)f;

         System.out.println("value of float f = "+f);
         System.out.println("value of long = "+l);

         long L = 5678;
         float F = L;
         System.out.println("value of long L = "+ L);
         System.out.println("value of float = "+F);

    }
}
