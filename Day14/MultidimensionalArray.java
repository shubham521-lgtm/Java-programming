// package Day14;

public class MultidimensionalArray {
    public static void main(String[] args) {
        int [][]marks = {
            {80 , 78 , 89 },
            {98 , 89 , 76 },
            {99 , 92 , 91 }
        };

        System.out.println(marks[0][0]);
        System.out.println(marks[1][2]);
        System.out.println(marks[2][1]);

    }
}
